<b>Capítulo II
DE LAS MEDIDAS DE SEGURIDAD<br>

Artículo 9. </b><br>El sujeto obligado, responsable de la tutela y tratamiento del sistema de datos personales, adoptará los siguientes tipos de seguridad:
I. Física. Relativa a las medidas tendentes a proteger y dar mantenimiento óptimo a las instalaciones, equipos, soportes o sistemas de datos, cuyo objeto sea la prevención de riesgos causados por casos fortuitos o causas de fuerza mayor; <br>
 
II. Lógica. Relativa a la autenticación de las personas o usuarios autorizados para el tratamiento de datos personales, de acuerdo con su función; <br>
 
III. De comunicaciones y redes informáticas. Tiene por objeto establecer las restricciones preventivas o de riesgos que deberán observar los usuarios de sistemas de datos personales para acceder a dominios o instalar programas informáticos autorizados, así como para el manejo de telecomunicaciones; <br>
 
IV. De cifrado. Tiene por objeto la implementación de algoritmos, claves, contraseñas, así como los dispositivos concretos de protección que garanticen la integridad y confidencialidad de la información, y <br>
 
V. De desarrollo y aplicaciones. Relativa a las autorizaciones requeridas para la creación o tratamiento de datos personales, para garantizar el adecuado desarrollo y uso de los mismos, previendo la participación de los usuarios, la separación de entornos, las consideraciones especiales relativas a las aplicaciones y las pruebas realizadas a éstas, la metodología a seguir y los ciclos de vida de los sistemas de datos <br>
<br><br>

<b>SECCIÓN SEGUNDA DE LOS NIVELES DE SEGURIDAD<br>
Artículo 10. </b><br>Además de los tipos de seguridad a que se refiere el artículo anterior, el responsable de la tutela y tratamiento del sistema de datos personales, adoptará y observará los siguientes niveles de seguridad: <br>
 
I. Básico. El que comprende la elaboración de la normativa que contenga las medidas generales de seguridad cuya aplicación es obligatoria para todos los sistemas de datos personales, mediante la emisión del Documento de Seguridad que habrán de observar todos los servidores públicos del sujeto obligado, así como para aquellas personas que con motivo de la prestación de un servicio, tengan acceso a los sistemas de datos personales o al sitio donde éstos se ubican. El documento de seguridad deberá contener, cuando menos: <br>
 
a) Nombre del sistema; b) Cargo y adscripción del responsable; c) Ámbito de aplicación; d) Estructura y descripción del Sistema de Datos Personales; e) Especificación detallada de la categoría de datos personales contenidos en el Sistema; <br>
 
f) Funciones y obligaciones del personal que intervenga en el tratamiento de los sistemas de datos personales. El titular u órgano de dirección del sujeto obligado, en coordinación con el responsable del Sistema de Datos Personales, adoptarán las medidas necesarias para que los servidores públicos del sujeto obligado conozcan las normas de seguridad y las responsabilidades y consecuencias en que se pudiera incurrir en caso de incumplimiento; <br>
 
g) Medidas, normas, procedimientos y criterios para garantizar el nivel de seguridad; <br>
 
h) Registro de incidencias. Para tal efecto se hará constar el momento y el tipo de incidencia ocurrida y se establecerán los procedimientos de notificación, gestión y respuesta. […]<br>
