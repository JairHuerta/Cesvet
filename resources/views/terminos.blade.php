<b>CAPÍTULO III DE LA DENUNCIA<br>

Artículo 12. </b><br>Cualquier persona podrá presentar una denuncia ante la Comisión, solicitando una investigación con motivo de una probable violación a las disposiciones establecidas en esta Ley, siempre y cuando no se encuentre vinculada con el ejercicio de los derechos ARCO (Acceso, Rectificación, Cancelación y Oposición de Datos Personales).<br>

Toda denuncia presentada ante la Comisión, deberá cumplir cuando menos con los requisitos siguientes:<br>

I. EI nombre del denunciante o, en su caso, la autoridad que promueve la denuncia;<br>

II. En caso de que la denuncia sea presentada por el interesado, deberá aportar los documentos que acrediten su identidad;<br>

III. Nombre del sujeto obligado que es denunciado;<br>

IV. Descripción clara y precisa del acto o resolución que motiva la denuncia, especificando el artículo y, en su caso, la fracción de la Ley que se considera no fueron observados;<br>

V. Las pruebas que tengan relación directa con el acto por el que se inconforma, y<br>

VI. El domicilio del denunciante o correo electrónico para recibir notificaciones.<br>

En caso de que no se señale domicilio alguno, o bien se señala un domicilio fuera del Estado de Tlaxcala, las notificaciones se practicarán a través de los estrados o de la página de internet de la Comisión, pudiendo el denunciante autorizar a terceras personas para recibir notificaciones y documentos en su nombre.<br>

La Comisión deberá prevenir al denunciante de las deficiencias de forma y fondo que contenga su escrito de denuncia.


