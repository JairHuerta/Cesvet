<b>Capítulo I
DE LOS PRINCIPIOS DE PROTECCIÓN DE DATOS PERSONALES</b><br>
Artículo 6.<br> Los responsables de los sistemas de datos personales en posesión de las entidades públicas, deberán observar los principios siguientes: <br><br>
 
<b>Licitud:</b> Consiste en que la posesión y tratamiento de sistemas de datos personales obedecerá exclusivamente a las atribuciones legales o reglamentarias de cada sujeto obligado, y deberán obtenerse a través de medios previstos en dichas disposiciones. <br>
 
Los sistemas de datos personales no pueden tener finalidades contrarias a las leyes o a la moral pública y en ningún caso pueden ser utilizados para finalidades distintas o incompatibles con aquella que motivaron su obtención. No se considerará incompatible el tratamiento posterior de éstos con fines históricos, estadísticos o científicos. <br>
 
<b>Consentimiento: </b> Consistente en que el tratamiento de datos personales debe estar sujeto a la manifestación de la voluntad libre, expresa, inequívoca, específica e informada del interesado. <br>
 
<b>Calidad de los Datos: </b> Consiste en que los datos personales recabados deben ser exactos, ciertos, adecuados, pertinentes y no excesivos en relación al ámbito y finalidad para los que se hubieren obtenido y respecto a las atribuciones legales del sujeto obligado que los posea.  <br>
 
<b>Confidencialidad: </b>Consistente en que, de manera exclusiva, la persona interesada acceda a los datos personales o, en su caso, el responsable o el usuario del sistema de datos personales para su tratamiento, guardando el responsable del sistema como los usuarios, la secrecía debida. <br>
 
Atendiendo al principio de confidencialidad, los datos personales adquieren el carácter de irrenunciables, intransferibles e indelegables, por lo que no podrán transmitirse salvo disposición legal o cuando medie el consentimiento del interesado.  <br>
 
La obligación de confidencialidad subsistirá aún después de finalizada la relación entre el sujeto obligado con el titular de los datos personales, así como después de finalizada la relación laboral entre el sujeto obligado y el responsable del sistema de datos personales o los usuarios. <br>
 
Los instrumentos jurídicos relativos a la contratación de servicios del encargado del sistema de datos personales, así como de los usuarios, deberán prever la obligación de garantizar la seguridad y confidencialidad de los sistemas de datos personales, así como la prohibición de utilizarlos con propósitos distintos para los cuales se llevó a cabo la contratación y las sanciones a que se hagan acreedores quienes incumplan con el principio de confidencialidad. Lo anterior, sin perjuicio de las responsabilidades penales, civiles o administrativas previstas en otras disposiciones aplicables. <br>
 
El responsable del sistema de datos personales o los usuarios podrán ser relevados  del  deber  de  confidencialidad  por  resolución  judicial y cuando se anteponga la salud pública o la seguridad nacional, estatal o municipal. <br>
 
<b>Seguridad: </b>Consiste en la adopción de medidas que garanticen, por parte del responsable, la integridad, confiabilidad, confidencialidad y disponibilidad de los datos personales mediante acciones que eviten su alteración, pérdida, transmisión y acceso no autorizado. <br>
 
<b>Disponibilidad: </b>Esto es que, los datos personales sean almacenados de modo que permitan el ejercicio de los derechos de acceso, rectificación, cancelación y oposición del interesado. <br>
 
<b>Temporalidad: </b>Consiste en que los datos personales deben ser destruidos cuando hayan dejado de ser necesarios o pertinentes a los fines para los que hubiesen sido recolectados, excepto que éstos sean tratados con objetivos estadísticos o científicos, siempre que cuenten con el procedimiento de disociación. <br>
 
Los datos personales que tengan fines históricos podrán ser conservados de manera íntegra y permanente. <br>
